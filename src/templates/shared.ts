/**
 * Generate a standard .gitignore for Node projects.
 */
export function generateGitignore(): string {
  return `node_modules/
dist/
.env
*.db
*.db-wal
*.db-shm
`
}

/**
 * Generate a standard tsconfig.json for ESM Node16 projects.
 */
export function generateTsConfig(): string {
  const config = {
    compilerOptions: {
      target: 'ES2022',
      module: 'Node16',
      moduleResolution: 'Node16',
      outDir: './dist',
      rootDir: '.',
      strict: true,
      esModuleInterop: true,
      skipLibCheck: true,
      declaration: true,
    },
    include: ['server.ts'],
  }

  return JSON.stringify(config, null, 2) + '\n'
}
